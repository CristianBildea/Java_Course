package ca.javacourse.Class_6;

public class Switch_case_statement_int {

    public static void main(String[] args) {

        int x = 4;

        switch (x){
            case 3:
                System.out.println("It's 3");
                break;
            case 4:
                System.out.println("Its still 3");
        }
    }
}
